class Solution {
public:
    vector<vector<int>> insert(vvi& intervals, vi& newInterval) {
        
        vvi ans;
        
        int i=0;
        int n=intervals.size();
        
        // adding all the non-overlapping intervals before the newInterval
        while(i<n and intervals[i][1]<newInterval[0]){
            ans.pb(intervals[i]);
            i++;
        }
        
        // finding the newInterval to be added
        while(i<n and intervals[i][0]<=newInterval[1]){
            newInterval[0]=min(newInterval[0],intervals[i][0]);
            newInterval[1]=max(newInterval[1],intervals[i][1]);
            i++;
        }
        // finally adding the newInterval 
        ans.pb(newInterval);
        
        //adding all the remaining intervals to the result vector
        while(i<n){
            ans.pb(intervals[i]);
            i++;
        }
        
        return ans; 
    
    }
};